main() {
  var someNumber, otherNumber;
  var total;
  someNumber = 15;
  otherNumber = 2;

  total = someNumber + otherNumber;
  print(total);

  total = someNumber - otherNumber;
  print(total);

  total = someNumber * otherNumber;
  print(total);

  total = someNumber / otherNumber;
  print(total);

  total = someNumber ~/ otherNumber;
  print(total);

  total = someNumber % otherNumber;
  print(total);

  total = -someNumber;
  print(total);
}
